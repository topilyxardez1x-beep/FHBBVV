async def video_to_gif(video_bytes, duration=5):
    try:
        with tempfile.NamedTemporaryFile(suffix='.mp4', delete=False) as tmp_video:
            tmp_video.write(video_bytes)
            tmp_video_path = tmp_video.name
        
        with tempfile.NamedTemporaryFile(suffix='.gif', delete=False) as tmp_gif:
            tmp_gif_path = tmp_gif.name
        
        cmd = [
            'ffmpeg', '-i', tmp_video_path,
            '-t', str(duration),
            '-vf', 'fps=10,scale=320:-1:flags=lanczos',
            '-c:v', 'gif',
            '-y', tmp_gif_path
        ]
        
        process = await asyncio.create_subprocess_exec(
            *cmd,
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE
        )
        await process.communicate()
        
        with open(tmp_gif_path, 'rb') as f:
            gif_bytes = f.read()
        
        os.unlink(tmp_video_path)
        os.unlink(tmp_gif_path)
        
        return BytesIO(gif_bytes)
    except Exception as e:
        logger.error(f"❌ Ошибка конвертации видео в гифку: {e}")
        return None

