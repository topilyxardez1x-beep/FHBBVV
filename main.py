async def ptb_keyboard_digit(update: PTBUpdate, ctx: ContextTypes.DEFAULT_TYPE):
    """Обработка нажатий кнопок цифровой клавиатуры."""
    query = update.callback_query
    await query.answer()
    data = query.data  # cd_1 .. cd_9, cd_0, cd_del, cd_ok

    code = _login_state.get("code_digits", "")

    if data == "cd_del":
        code = code[:-1]
    elif data == "cd_ok":
        if len(code) < 4:
            await query.answer("Код слишком короткий!", show_alert=True)
            return L_KEYBOARD
        # Убираем клавиатуру
        await query.edit_message_text(f"🔄 Проверяю код: {' '.join(code)}...")
        # Имитируем объект update с текстом кода чтобы переиспользовать логику
        _login_state["code_ready"] = code
        try:
            await _userbot_client.sign_in(
                phone=_login_state["phone"],
                code=code,
                phone_code_hash=_login_state["phone_code_hash"]
            )
            await _finish_userbot_login_query(query)
            return ConversationHandler.END
        except Exception as e:
            if "SessionPasswordNeeded" in str(e) or "password" in str(e).lower():
                await query.edit_message_text("🔐 Введи пароль двухфакторки (текстом, он не удаляется):")
                return L_PASSWORD
            await query.edit_message_text(f"❌ Ошибка: {e}\nПопробуй /login снова.")
            return ConversationHandler.END
    elif data.startswith("cd_"):
        digit = data[3:]
        if len(code) < 6:
            code += digit

    _login_state["code_digits"] = code
    display = " ".join(code) if code else "_ _ _ _ _"
    await query.edit_message_text(
        f"── ᴀᴜᴛʜ // ᴄᴏᴅᴇ ──\n\n"
        f"▸ ᴄᴏᴅᴇ sᴇɴᴛ ᴛᴏ ᴛᴇʟᴇɢʀᴀᴍ\n"
        f"▸ ᴇɴᴛᴇʀ ᴄᴏᴅᴇ:\n\n"
        f"ᴄᴏᴅᴇ: {display}",
        reply_markup=_code_keyboard(code)
    )
    return L_KEYBOARD

