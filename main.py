async def _run_ptb():
    global _ptb_app
    _ptb_app = Application.builder().token(STATUS_BOT_TOKEN).build()

    login_conv = ConversationHandler(
        entry_points=[PTBCommandHandler("login", ptb_login)],
        per_message=False,
        per_chat=True,
        per_user=True,
        states={
            L_PHONE:    [PTBMessageHandler(filters.TEXT & ~filters.COMMAND, ptb_got_phone)],
            L_KEYBOARD: [CallbackQueryHandler(ptb_keyboard_digit, pattern="^cd_")],
            L_PASSWORD: [PTBMessageHandler(filters.TEXT & ~filters.COMMAND, ptb_got_password)],
        },
        fallbacks=[PTBCommandHandler("cancel", ptb_cancel)],
    )

    _ptb_app.add_handler(login_conv)
    _ptb_app.add_handler(PTBCommandHandler("start",      ptb_start))
    _ptb_app.add_handler(PTBCommandHandler("status",     ptb_status))

    await _ptb_app.initialize()
    await _ptb_app.start()

    await _ptb_app.bot.delete_webhook(drop_pending_updates=True)

    # Запускаем polling с авто-перезапуском при Conflict
    async def _start_polling_loop():
        while True:
            try:
                if _ptb_app.updater.running:
                    await _ptb_app.updater.stop()
                await _ptb_app.updater.start_polling(
                    drop_pending_updates=True,
                    allowed_updates=["message", "callback_query"]
                )
                logger.info("✅ PTB polling запущен")
                return
            except Exception as e:
                if "Conflict" in str(e):
                    logger.warning("⚠️ Conflict — старый инстанс ещё жив, ждём 5 сек...")
                    await asyncio.sleep(5)
                else:
                    logger.error(f"❌ Polling ошибка: {e}")
                    await asyncio.sleep(3)

    asyncio.create_task(_start_polling_loop())
    logger.info("✅ PTB бот запущен, жди команды /login или /start")

